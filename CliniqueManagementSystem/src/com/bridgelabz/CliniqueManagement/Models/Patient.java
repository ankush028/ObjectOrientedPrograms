package com.bridgelabz.CliniqueManagement.Models;

public class Patient {
private String name;
private int id;
private String pmbno;
private int age;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getPmbno() {
	return pmbno;
}
public void setPmbno(String pmbno) {
	this.pmbno = pmbno;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}

}
